function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function randomFio(): string {
  const set = Math.random() < 0.5 ? MALE : FEMALE;
  return `${pick(set.last)} ${pick(set.first)} ${pick(set.middle)}`;
}

export function randomPhoneDigits(): string {
  // 10 digits, starting with 9 (Russian mobile prefix).
  let s = "9";
  for (let i = 0; i < 9; i++) s += Math.floor(Math.random() * 10);
  return s;
}
